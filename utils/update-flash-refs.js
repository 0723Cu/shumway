var fs = require('fs');
var path = require('path');

function updateLibRefs(filePath, manifestPath) {
  var content = fs.readFileSync(filePath, 'utf8');
  var m = new RegExp('\\n([\\t\\u0020]+)<!-- Autogenerated references: base=(\\S+)').exec(content);
  if (!m) {
    throw new Error(filePath + ' has no references');
  }

  var baseUrl = m[2], indent = m[1];
  var lineStart = indent + '<script src="' + baseUrl;
  var lineEnd = '"></script>';

  var entries = fs.readFileSync(manifestPath).toString().split(/\r?\n/g);

  content = content.replace(new RegExp('(Autogenerated references:.*?\\n)[\\S\\s]*?(\\n[ \t]+<!-- Autogenerated references end)'),
    function (all, head, tail) {
      return head + entries.map(function (entry) {
        if (entry.trim() === '') {
          return '';
        }
        if (entry[0] !== '#') {
          return indent + '<script src="' + baseUrl + entry + '"></script>';
        }
        if (entry[1] === '!') {
          if (entry.indexOf('#!inline ') === 0) {
            return indent + '<script> ' + entry.substr(9).trim() + ' </script>';
          }
          console.error('Skipping unknown ' + entry);
          return;
        }
        if (entry[1] === '#') {
          return indent + '<!-- ' + entry.substr(2).trim() + ' -->';
        }
        if (entry[1] !== ' ') {
          return indent + '<!--<script src="' + baseUrl + entry.substr(1) + '"></script>-->';
        }
        return '';
      }).join('\n') + tail;
    }
  );

  fs.writeFileSync(filePath, content, 'utf8');
}

function updateJSLibRefs(filePath, manifestPath) {
  var content = fs.readFileSync(filePath, 'utf8');
  var m = new RegExp('<!-- Autogenerated references: base=(\\S+)').exec(content);
  if (!m) {
    throw new Error(filePath + ' has no references');
  }

  var baseUrl = m[1], indent = "";
  var lineStart = indent + '<script src="' + baseUrl;
  var lineEnd = '"></script>';

  var entries = fs.readFileSync(manifestPath).toString().split(/\r?\n/g);

  content = content.replace(new RegExp('(Autogenerated references:.*?\\n)[\\S\\s]*?(\\n<!-- Autogenerated references end)'),
    function (all, head, tail) {
      return head + entries.map(function (entry) {
        if (entry.trim() === '') {
          return '';
        }
        if (entry[0] !== '#') {
          return indent + 'load("' + baseUrl + entry + '");';
        }
        if (entry[1] === '!') {
          if (entry.indexOf('#!inline ') === 0) {
            return indent + entry.substr(9).trim();
          }
          console.error('Skipping unknown ' + entry);
          return;
        }
        if (entry[1] === '#') {
          return indent + '// ' + entry.substr(2).trim();
        }
        if (entry[1] !== ' ') {
          return indent + '//' + baseUrl + entry.substr(1);
        }
        return '';
      }).join('\n') + tail;
    }
  );

  fs.writeFileSync(filePath, content, 'utf8');
}

function endsWith(string, value) {
  return string.substring(string.length - value.length, string.length) === value;
}

function updateFlashRefs(filePath, srcDir) {
  if (endsWith(filePath, ".js")) {
    updateJSLibRefs(filePath, srcDir + '/shumway.package');
  } else {
    updateLibRefs(filePath, srcDir + '/shumway.package');
  }
}

module.exports = updateFlashRefs;